create or replace function public.fp_award_early_build_badge()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if coalesce((select (value::text)::boolean from public.platform_settings where key='early_build'), true) = true then
    insert into public.user_badges(user_id,badge_code)
    values(new.id,'flashportal_pioneer')
    on conflict(user_id,badge_code) do nothing;
  end if;
  insert into public.user_badges(user_id,badge_code)
  values(new.id,'profile_started')
  on conflict(user_id,badge_code) do nothing;
  return new;
end;
$$;

