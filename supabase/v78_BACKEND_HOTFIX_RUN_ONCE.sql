drop function if exists public.fp_delete_social_comment(bigint);

create or replace function public.fp_delete_social_comment(target_comment_id bigint)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  uid uuid := auth.uid();
  target_post bigint;
begin
  if uid is null then
    raise exception 'login_required';
  end if;

  select post_id into target_post from public.social_comments where id=target_comment_id;
  if target_post is null then
    return false;
  end if;

  update public.social_comments sc
  set is_deleted=true
  where sc.id=target_comment_id
    and (
      sc.user_id=uid
      or exists(select 1 from public.social_posts sp where sp.id=sc.post_id and sp.user_id=uid)
    );

  update public.social_posts sp
  set comments=(
    select count(*)::integer
    from public.social_comments sc
    where sc.post_id=sp.id and coalesce(sc.is_deleted,false)=false
  )
  where sp.id=target_post;

  return true;
end;
$$;
grant execute on function public.fp_delete_social_comment(bigint) to authenticated;

create or replace function public.fp_set_social_reaction(target_post_id bigint, new_reaction text)
returns table(likes integer, dislikes integer, my_reaction text)
language plpgsql
security definer
set search_path = public
as $$
declare
  uid uuid := auth.uid();
  clean_reaction text := nullif(trim(lower(new_reaction)), '');
  like_count integer;
  dislike_count integer;
  final_reaction text;
begin
  if uid is null then
    raise exception 'login_required';
  end if;

  if clean_reaction not in ('like','dislike') and clean_reaction is not null then
    raise exception 'invalid_reaction';
  end if;

  if clean_reaction is null then
    delete from public.social_post_reactions where post_id=target_post_id and user_id=uid;
  elsif exists(select 1 from public.social_post_reactions where post_id=target_post_id and user_id=uid and reaction=clean_reaction) then
    delete from public.social_post_reactions where post_id=target_post_id and user_id=uid;
    clean_reaction := null;
  else
    insert into public.social_post_reactions(post_id,user_id,reaction,updated_at)
    values(target_post_id,uid,clean_reaction,now())
    on conflict(post_id,user_id)
    do update set reaction=excluded.reaction, updated_at=now();
  end if;

  select count(*)::integer into like_count from public.social_post_reactions where post_id=target_post_id and reaction='like';
  select count(*)::integer into dislike_count from public.social_post_reactions where post_id=target_post_id and reaction='dislike';
  select reaction into final_reaction from public.social_post_reactions where post_id=target_post_id and user_id=uid;

  update public.social_posts set likes=like_count, dislikes=dislike_count where id=target_post_id;

  insert into public.user_badges(user_id,badge_code)
  values(uid,'first_like')
  on conflict(user_id,badge_code) do nothing;

  return query select like_count, dislike_count, final_reaction;
end;
$$;
grant execute on function public.fp_set_social_reaction(bigint,text) to authenticated;

create or replace function public.fp_get_creator_feed(feed_limit integer default 25)
returns table(
  id bigint,
  user_id uuid,
  body text,
  image_url text,
  video_url text,
  media_type text,
  created_at timestamptz,
  likes integer,
  dislikes integer,
  comments integer,
  my_reaction text,
  author_username text,
  author_display_name text,
  author_avatar_url text,
  author_is_private boolean,
  author_last_seen_at timestamptz
)
language sql
security definer
set search_path = public
as $$
  select
    sp.id,
    sp.user_id,
    sp.body,
    sp.image_url,
    sp.video_url,
    sp.media_type,
    sp.created_at,
    coalesce(sp.likes,0)::integer,
    coalesce(sp.dislikes,0)::integer,
    coalesce(sp.comments,0)::integer,
    spr.reaction,
    up.username,
    up.display_name,
    up.avatar_url,
    coalesce(up.is_private,false),
    up.last_seen_at
  from public.social_posts sp
  left join public.user_profiles up on up.id=sp.user_id
  left join public.social_post_reactions spr on spr.post_id=sp.id and spr.user_id=auth.uid()
  where coalesce(sp.is_deleted,false)=false
    and coalesce(sp.is_private,false)=false
    and coalesce(up.is_deleted,false)=false
  order by sp.created_at desc
  limit feed_limit;
$$;
grant execute on function public.fp_get_creator_feed(integer) to anon, authenticated;

create or replace function public.fp_get_achievement_page(target_user_id uuid default null)
returns table(
  code text,
  title text,
  rarity text,
  category text,
  game_id text,
  description text,
  unlock_hint text,
  points integer,
  is_hidden boolean,
  earned boolean,
  earned_at timestamptz
)
language sql
security definer
set search_path = public
as $$
  select
    ac.code,
    ac.title,
    ac.rarity,
    ac.category,
    ac.game_id,
    ac.description,
    ac.unlock_hint,
    ac.points,
    ac.is_hidden,
    (ub.id is not null) as earned,
    ub.earned_at
  from public.achievement_catalog ac
  left join public.user_badges ub
    on ub.badge_code=ac.code
   and ub.user_id=coalesce(target_user_id, auth.uid())
  where ac.is_hidden=false or ub.id is not null
  order by
    case ac.rarity
      when 'legacy' then 0
      when 'mythic' then 1
      when 'legendary' then 2
      when 'epic' then 3
      when 'rare' then 4
      when 'uncommon' then 5
      else 6
    end,
    ac.points desc,
    ac.title asc;
$$;
grant execute on function public.fp_get_achievement_page(uuid) to anon, authenticated;

-- Backfill counters so UI matches the database after refresh.
update public.social_posts sp
set likes = coalesce(counts.total,0)
from (
  select post_id, count(*)::integer as total
  from public.social_post_reactions
  where reaction='like'
  group by post_id
) counts
where sp.id = counts.post_id;

update public.social_posts sp
set dislikes = coalesce(counts.total,0)
from (
  select post_id, count(*)::integer as total
  from public.social_post_reactions
  where reaction='dislike'
  group by post_id
) counts
where sp.id = counts.post_id;

update public.social_posts sp
set comments = coalesce(counts.total,0)
from (
  select post_id, count(*)::integer as total
  from public.social_comments
  where coalesce(is_deleted,false)=false
  group by post_id
) counts
where sp.id = counts.post_id;

